        private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

            btnNovo.setEnabled(true);
            btnLimpar.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnUpload.setEnabled(false);

            txtCep.setEnabled(false);
            txtCpf.setEnabled(false);
            txtEmail.setEnabled(false);
            txtIdade.setEnabled(false);
            txtNome.setEnabled(false);
            cmbSexo.setEnabled(false);
            txtTelefone.setEnabled(false);
            txtEndereco.setEnabled(false);
            txtSenha.setEnabled(false);


            if (txtCep.getText().isEmpty() || txtCpf.getText().isEmpty() ||
                    txtEmail.getText().isEmpty() || txtEndereco.getText().isEmpty() || txtEndereco.getText().isEmpty()
                    || txtIdade.getText().isEmpty() || txtNome.getText().isEmpty() || txtSenha.getText().isEmpty() || txtTelefone.getText().isEmpty()
                    || txtSenha.getText().isEmpty()) {


                txtCep.setEnabled(true);
                txtCpf.setEnabled(true);
                txtEmail.setEnabled(true);
                txtEndereco.setEnabled(true);
                txtIdade.setEnabled(true);
                txtNome.setEnabled(true);
                txtSenha.setEnabled(true);

                cmbSexo.setEnabled(true);
                txtTelefone.setEnabled(true);
                btnNovo.setEnabled(false);
                btnLimpar.setEnabled(true);
                btnSalvar.setEnabled(true);
                btnUpload.setEnabled(true);

                JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!!!", "Aten√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!!", "Cadastrado", JOptionPane.INFORMATION_MESSAGE);

                Funcionario funcionario = new Funcionario();
                funcionario.setNome(txtNome.getText());
                funcionario.setCpf(txtCpf.getText());
                funcionario.setEmail(txtEmail.getText());
                funcionario.setEndereco(txtEndereco.getText());
                funcionario.setFoto(getImagemSelecionada);
                funcionario.setIdade(txtIdade.getText());
                funcionario.setNome(txtNome.getText());
                funcionario.setSenha(txtSenha.getText());
                funcionario.setSexo(String.valueOf(cmbSexo.getSelectedItem()));
                funcionario.setTelefone(txtTelefone.getText());

                cadastrosPersonal.add(funcionario);

                icon.setIcon(null);
                txtCep.setText("");
                txtCpf.setText("");
                txtEmail.setText("");
                txtEndereco.setText("");
                txtIdade.setText("");
                txtNome.setText("");
                txtTelefone.setText("");
                txtSenha.setText("");

            }

        }//GEN-LAST:event_btnSalvarActionPerformed

